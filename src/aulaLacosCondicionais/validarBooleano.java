package aulaLacosCondicionais;

public class validarBooleano {

	public static void main(String[] args) {
		
		boolean verdade = true;
		boolean falso = false;
		
		if (verdade == true) {
			System.out.println("a variável verdade é verdadeira.");
		}
		
		if (!falso) {
			System.out.println("a variável falso é verdadeira.");
		}

	}

}
